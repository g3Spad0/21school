int		handler(char *str, int i)
{
	int		n;

	n = 0;
	while (str[i])
	{
		if ((i + 1) % 21 == 0 && ++n)
		{
			if (str[i] != '\n')
				return (0);
			str[i] = 'D';
		}
		else if ((i - n + 1) % 5 == 0)
		{
			if (str[i] != '\n')
				return (0);
		}
		else if (str[i] != '#' && str[i] != '.')
			return (0);
		i++;
	}
	return (((i + 1) % 21 == 0) ? n + 1 : 0);
}
