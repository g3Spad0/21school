int		pf_atoi(const char *str)
{
	int		num;

	num = 0;
	if (str[0] == '.')
		str++;
	if (str[0] == '-' || str[1] == '+')
		return (0);
	while (*str >= '0' && *str <= '9')
		num = (num * 10) + (*str++ - '0');
	return (num);
}
