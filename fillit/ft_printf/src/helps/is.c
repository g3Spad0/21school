#include "../../includes/ft_printf.h"

int				pf_is_num(int c)
{
	return (c == 'd' || c == 'i' || c == 'o' ||
			c == 'u' || c == 'x' || c == 'X' ||
			c == 'f' ? c : 0);
}

int				pt_is_numbers(int c)
{
	return (c == 'd' || c == 'i' || c == 'o' ||
			c == 'u' || c == 'x' || c == 'X'
			? c : 0);
}

int				pf_is_minus(int c)
{
	return (c == 'u' || c == 'o' || c == 'x' || c == 'X' ? 1 : 0);
}

int				pf_is_for_sharp(int c)
{
	return (c == 'x' || c == 'X' || c == 'o'
			|| c == 'f' ? 1 : 0);
}
